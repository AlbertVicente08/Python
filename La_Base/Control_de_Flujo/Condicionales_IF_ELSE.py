"""
Condicionales: if / else
Es la capacidad de tu programa para tomar decisiones.
El código elige un camino u otro dependiendo de si una condición es True o False.

Sintaxis:
- Uso de dos puntos (:) al final de la condición.
- Indentación (sangría) obligatoria para el código que pertenece al bloque.
"""

edad = 20

if edad >= 18:
    print("Eres mayor de edad")  # Se ejecuta si es True
    print("Puedes pasar")
else:
    print("Eres menor de edad")  # Se ejecuta si es False
    print("No puedes pasar")

# Existe también 'elif' para múltiples opciones intermedias.
