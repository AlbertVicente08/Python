
#Int Son numeros sin decimales
vidas = 3
anio = 2024
temperatura = -5

#Float Son numeros con parte decimal
pi = 3.1416
altura = 1.75

#Str Es texto
nombre = "Guido van Rossum"
letra = 'a'
numero_como_texto = "100"  # ¡Ojo! Esto es texto, no un número matemático.

#Booleanos Representa valores de verdad logica True/False
es_de_dia = True
esta_lloviendo = False

#Tipado Dinamico  No necesitas declarar el tipo de dato explícitamente
caja = 10       # Ahora 'caja' es un entero (int)
print(caja)

caja = "Manzana" # ¡Ahora 'caja' es un string (str)!
print(caja)

#type() para saber el tipo de dato que es
x = 9.5
print(type(x))  # Salida: <class 'float'>